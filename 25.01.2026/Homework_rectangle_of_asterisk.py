c: int = int(input("Enter the column number: "))
r: int = int(input("Enter the row number: "))
d = 1

print ("*"*c)
#print column

while r > d:
#as long as rows if higher than d (the counter)

    print ("*"*c)
#print the same amount of column in a new row

    d += 1
#keeps the numbers of rows right